namespace CoreUI
{
    public enum InputState
    {
        //
        // Summary:
        //     The key or mouse button was released.
        Release = 0,
        //
        // Summary:
        //     The key or mouse button was pressed.
        Press = 1,
        //
        // Summary:
        //     The key was held down until it repeated.
        Repeat = 2
    }
}